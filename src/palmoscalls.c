void palmosInstrNotify(uint32_t instr, uint32_t lr) {
    static int tab = -1;

    if ((tab > 0) && ((instr & 0xfffff000ul) == 0xe59cf000ul)) {
        const struct OsCall *c;

        for (c = mPalmOsCalls; c->name; c++) {
            if (c->tab != tab) continue;
            if (c->func != (instr & 0xfff)) continue;
            break;
        }

        if (c->name)
            fprintf(stderr, "OSCALL %s from 0x%08lx\n", c->name, (unsigned long)lr);
        else
            fprintf(stderr, "OSCALL {%d, 0x%04x} from 0x%08lx\n", tab, (unsigned)(instr & 0xfff),
                    (unsigned long)lr);
    }
    tab = ((instr & 0xfffffff0ul) == 0xe519c000ul) ? (int)(instr & 0x0f) : -1;
}
